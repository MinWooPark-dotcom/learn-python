# 문자열의 구성 파악하기: isOO()

# 문자열이 알파벳 또는 숫자로만 구성되어 있는지 확인
print("alphabet123".isalnum())

# 문자열이 알파벳으로만 구성되어 있는지 확인
print("onlyAlphabet".isalpha())

# 문자열이 식별자로 사용할 수 있는 것인지 확인
print("dentifier".isidentifier())

# 문자열이 정수 형태인지 확인
print("123".isdecimal())

# 문자열이 숫자로 인식될 수 있는 것인지 확인
print('123'.isdigit())

# 문자열이 공백으로만 구성되어 있는지 확인
print('  '.isspace())

# 문자열이 소문자로만 구성되어 있는지 확인
print('lower'.islower())

# 문자열이 대문ㄴ자로만 구성되어 있는지 확인
print('ISUPPER'.isupper())
