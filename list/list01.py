# Declear list
list_a = [1, 2, 3]
list_b = [4, 5, 6]

# print
print("# list")
print(list_a)
print(list_b)
print()

# basic operator
print('# list basic operator')
print('+', list_a + list_b)  # 문자열 연결 연산자 [1,2,3,4,5,6]
print('*', list_a * 3)  # 문자열 반복 연산자 [1,2,3,1,2,3,1,2,3]
print()

# function
print('get length')
print('len()', len(list_a))  # 3
